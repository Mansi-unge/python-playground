# Write a program to display student details using:
# • format() method
# • f-string

name = "mansi sanjay unge"
course = "MCA"
year = "first year"

# format() method
print("{} is the student of {} department of {}".format(name,course,year))

# f-string method
print(f"{name} is the student of {course} department of {year}")
